/**
 * テキストにふりがなを付与する関数
 * @param {string} text - 入力テキスト
 * @returns {Array<{text: string, reading: string|null}>} - テキストと読みのペア配列
 */
function addFurigana(text) {
  const result = [];
  let i = 0;

  while (i < text.length) {
    // ひらがな・カタカナ・英数字・記号はそのまま
    if (!isKanji(text[i])) {
      let plain = "";
      while (i < text.length && !isKanji(text[i])) {
        plain += text[i];
        i++;
      }
      result.push({ text: plain, reading: null });
      continue;
    }

    // 漢字: 最長一致で複合語を探す
    let matched = false;
    const maxLen = Math.min(4, text.length - i);
    for (let len = maxLen; len >= 2; len--) {
      const substr = text.substring(i, i + len);
      if (COMPOUND_DICT[substr]) {
        result.push({ text: substr, reading: COMPOUND_DICT[substr] });
        i += len;
        matched = true;
        break;
      }
    }
    if (matched) continue;

    // 単漢字で検索
    if (KANJI_DICT[text[i]]) {
      result.push({ text: text[i], reading: KANJI_DICT[text[i]] });
    } else {
      // 辞書にない漢字はそのまま
      result.push({ text: text[i], reading: null });
    }
    i++;
  }

  return result;
}

/**
 * CJK統合漢字かどうか判定
 */
function isKanji(ch) {
  const code = ch.charCodeAt(0);
  return (code >= 0x4E00 && code <= 0x9FFF) ||
         (code >= 0x3400 && code <= 0x4DBF) ||
         (code >= 0x20000 && code <= 0x2A6DF);
}

/**
 * ルビHTML形式に変換
 */
function toRubyHTML(segments) {
  return segments.map(seg => {
    if (seg.reading) {
      return `<ruby>${escapeHTML(seg.text)}<rp>(</rp><rt>${escapeHTML(seg.reading)}</rt><rp>)</rp></ruby>`;
    }
    return escapeHTML(seg.text);
  }).join("");
}

/**
 * 括弧形式に変換
 */
function toParenText(segments) {
  return segments.map(seg => {
    if (seg.reading) {
      return `${seg.text}（${seg.reading}）`;
    }
    return seg.text;
  }).join("");
}

/**
 * カタカナ形式に変換
 */
function toKatakanaText(segments) {
  return segments.map(seg => {
    if (seg.reading) {
      return `${seg.text}（${hiraganaToKatakana(seg.reading)}）`;
    }
    return seg.text;
  }).join("");
}

/**
 * ひらがな→カタカナ変換
 */
function hiraganaToKatakana(str) {
  return str.replace(/[\u3041-\u3096]/g, ch =>
    String.fromCharCode(ch.charCodeAt(0) + 0x60)
  );
}

/**
 * HTMLエスケープ
 */
function escapeHTML(str) {
  return str.replace(/&/g, "&amp;")
            .replace(/</g, "&lt;")
            .replace(/>/g, "&gt;")
            .replace(/"/g, "&quot;")
            .replace(/'/g, "&#039;");
}
